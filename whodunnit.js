
// Episode 1

const scenario = {
  murderer: 'Miss Scarlet',
  room: 'Library',
  weapon: 'Rope'
};

const declareMurderer = function() {
  return `The murderer is ${scenario.murderer}.`;
}

const verdict = declareMurderer();
console.log(verdict);

// The function declare murderer is a const function, which means it cannot be changed.
// The function is an anonymous function.
// The murderer key is accessed through the the scenario variable.
// the const variable 'verdict' is equal to the const function, and is printed.

// the result will be that Miss Scarlet is the murderer.

